cgp = 3.5;
switch (cgp) {
  case 5:
    console.log("A+");
    break;
  case 4:
    console.log("A");
    break;
  case 3.5:
    console.log("A-");
    break;
  case 3:
    console.log("B");
    break;
  case 2:
    console.log("C");
    break;
  case 1:
    console.log("E");
    break;
  default:
    console.log("Please enter valid number");
}
